"""
Programação Orientado a Objetos - POO
- POO é um paradigma da programação que utiliza como mapeamento de objetos no mundo real para modelos computacionais.
- Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas.

Principais elementos da Orientação a Objetos:
- Classe -> Modelo do objeto do mundo real, sendo representado computacionalmente.
- Atributo -> Caracteristicas do objeto.
- Método -> Compoartamento do objeto (função).
- Construtor -> Método especial utilizado para criar os objetos.
- Objeto -> Instância da classe.


"""

# Exemplo simples
numero = 10
print(numero)
print(type(numero))

nome = 'Uday'
print(nome)
print(type(nome))


class Produto:
    pass


ps4 = Produto()
print(ps4)
print(type(ps4))