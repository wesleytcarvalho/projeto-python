"""
Tipo None

O tipo None em python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado

Obs: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos?
- Podemos utilizar o None quando queremos criar uma variavel e inicializá-la com um tipo sem tipo, antes
de recebermos um valor final.

Obs: O tipo None em python e sempre considerado FALSE
"""
numero = None

print(numero)
print(type(numero))

numero = 1.44, 1.34, 5.67

print(numero)
print(type(numero))
